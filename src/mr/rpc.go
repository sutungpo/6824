package mr

//
// RPC definitions.
//
// remember to capitalize all names.
//

import (
	"os"
	"strconv"
)

//
// example to show how to declare the arguments
// and reply for an RPC.
//

type ExampleArgs struct {
	X int
}

type ExampleReply struct {
	Y int
}

type WorkArgs struct {
	S   string
	Seq int
	Ext map[string]int
}

type WorkReply struct {
	Error error
	Ts    Task
}

type Task struct {
	Ctg    string
	Detail []string
	Seq    int
	Ext    interface{}
}

